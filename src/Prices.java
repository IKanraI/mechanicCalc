public class Prices {
    private Double tuningPrice;
    private Double blindPrice;
    private Double cosmeticPrice;

    public Double getTuningPrice() {
        return tuningPrice;
    }

    public void setTuningPrice(Double tuningPrice) {
        this.tuningPrice = tuningPrice;
    }

    public Double getBlindPrice() {
        return blindPrice;
    }

    public void setBlindPrice(Double blindPrice) {
        this.blindPrice = blindPrice;
    }

    public Double getCosmeticPrice() {
        return cosmeticPrice;
    }

    public void setCosmeticPrice(Double cosmeticPrice) {
        this.cosmeticPrice = cosmeticPrice;
    }
}
